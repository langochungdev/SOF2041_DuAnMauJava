    void init(){
        setSize(1000,600);
        setIconImage(XImage.getAppIcon());
        setLocationRelativeTo(null);
        setTitle("HE THONG QUAN LY DAO TAO EDUSYS");
        new Timer(1000, new ActionListener() {
            SimpleDateFormat fm = new SimpleDateFormat("hh:mm:ss a");
            @Override
            public void actionPerformed(ActionEvent e) {
                lbDongHo.setText(fm.format(new Date()));
            }
        }).start();
        new ChaoD(this, true).setVisible(true);
        openLogin();
    }
    
    void openLogin(){
        new DangNhapD(this, true).setVisible(true);
    }
    
    void openDoiMK(){
        if(Auth.isLogin()){
            new DoiMatKhauD(this, true).setVisible(true);
        }else{
            MsgBox.alert(this, "Vui long dang nhap");
        }
    }
    
    void openChuyenDe(){
        if(Auth.isLogin()){
            new ChuyenDeD(this, true).setVisible(true);
        }else{
            MsgBox.alert(this, "Vui long dang nhap");
        }
    }
    
    void dangXuat(){
        Auth.clear();
        openLogin();
    }
    
    

